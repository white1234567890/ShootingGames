#pragma once
#include "common.h"

class ChildClass
{
protected:
	POSITION m_LocalPosition;	//ローカル中心座標
	VELOCITY m_LocalVelocity;	//ローカル速度
	ACCELARATION m_LocalAccelaration;	//ローカル加速度
	double m_LocalAngle;	//ローカル傾き

public:
	ChildClass(void);
	~ChildClass(void);

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	ローカル座標を取得する
	//戻り値:
	//	m_LocalPosition:ローカル中心座標
	//////////////////////////////////////////////////////////////////////////////
	POSITION GetLocalPosition();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	ローカル速度を取得する
	//戻り値:
	//	m_LocalVelocity:ローカル速度
	//////////////////////////////////////////////////////////////////////////////
	VELOCITY GetLocalVelocity();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	ローカル加速度を取得する
	//戻り値:
	//	m_LocalAccelaration:ローカル加速度
	//////////////////////////////////////////////////////////////////////////////
	ACCELARATION GetLocalAccelaration();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	ローカル傾きを取得する
	//戻り値:
	//	m_LocalAngle:ローカル傾き
	//////////////////////////////////////////////////////////////////////////////
	double GetLocalAngle();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	ローカル座標を設定する
	//引数:
	//	*position:設定するベクトル
	//////////////////////////////////////////////////////////////////////////////
	void SetLocalPosition(THREE_DIMENSION_VECTOR* position);
};

